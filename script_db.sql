CREATE DATABASE aerolinea;

USE aerolinea;

CREATE TABLE pasajeros (
  NM_ID INT NOT NULL AUTO_INCREMENT COMMENT 'CODIGO IDENTIFICADOR',
  NM_ID_VUELO INT NOT NULL COMMENT 'ID VUELO',
  DS_NOMBRES varchar(200) NOT NULL COMMENT 'NOMBRES',
  DS_APELLIDOS varchar(200) NOT NULL COMMENT 'APELLIDOS',
  DS_GENERO varchar(200) NOT NULL COMMENT 'GENERO',
  DS_TIPO_IDENTIFICACION varchar(200) NOT NULL COMMENT 'TIPO IDENTIFICACION',
  DS_IDENTIFICACION varchar(200) NOT NULL COMMENT 'NUMERO DE IDENTIFICACION',
  DS_CELULAR varchar(200) NOT NULL COMMENT 'NUMERO CELULAR',
  DS_EMAIL varchar(200) NOT NULL COMMENT 'CORREO ELECTRONICO',
  PRIMARY KEY (NM_ID));

CREATE TABLE vuelos (
  NM_ID INT NOT NULL AUTO_INCREMENT COMMENT 'CODIGO IDENTIFICADOR',
  NM_ID_PASAJERO INT NOT NULL COMMENT 'ID PASAJERO',
  NM_NUMERO_VUELO INT NOT NULL COMMENT 'NUMERO DE VUELO',
  DS_ORIGEN VARCHAR(200) NOT NULL COMMENT 'ORIGEN',
  DS_DESTINO VARCHAR(200) NOT NULL COMMENT 'DESTINO',
  DS_HORA VARCHAR(200) NOT NULL COMMENT 'HORA VUELO',
  FE_FECHA VARCHAR(200) NOT NULL COMMENT 'FECHA DE VUELO',
  PRIMARY KEY (NM_ID));

CREATE TABLE aviones (
  ID_AVION INT NOT NULL AUTO_INCREMENT COMMENT 'CODIGO IDENTIFICADOR',
  NM_ID_VUELO_PASAJERO INT NOT NULL COMMENT 'ID VUELO Y PASAJERO',
  DS_TIPO VARCHAR(200) NOT NULL COMMENT 'TIPO DE AVION',
  NM_NUMERO_PASAJEROS VARCHAR(45) NOT NULL COMMENT 'NUMERO DE PASAJEROS',
  PRIMARY KEY (ID_AVION));

